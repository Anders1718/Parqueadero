    private void jButtonListoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListoActionPerformed
        boolean sw2=false;
        if(jLabelId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No seleccionÃ³ el viaje a pagar");
            sw2=true;
        }
            
    
        else if(sw2==false){
            
            try {
                con = DriverManager.getConnection(url, user, clave);
                stmt = con.createStatement();
                stmt.executeUpdate("INSERT INTO viajes VALUES('0' , "
                        + "'"+jLabelFecha.getText()+"' , "
                        + "'"+jLabelPrecio.getText()+"' , "
                        + "'"+jLabelCliente.getText()+"' , "
                                + "'"+jLabelDescripcion.getText()+"')");
                } catch (SQLException ex) {
                    Logger.getLogger(RegistrarRecibo.class.getName()).log(Level.SEVERE, null, ex);
                }
            
            
            try {
                con = DriverManager.getConnection(url, user, clave);
                stmt = con.createStatement();
                stmt.executeUpdate("DELETE FROM viajes_pendientes WHERE id = '"+jLabelId.getText()+"'");
                } catch (SQLException ex) {
                    Logger.getLogger(RegistrarRecibo.class.getName()).log(Level.SEVERE, null, ex);
                }
            
            
            JOptionPane.showMessageDialog(null, "Registro exitoso");
            modelo.setRowCount(0);
            
            
            jLabelSumas.setText("");
            
            refrescarPagina();
        }
    }//GEN-LAST:event_jButtonListoActionPerformed

