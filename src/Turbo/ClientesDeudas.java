    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        boolean sw2=false;
        if(jLabelId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No seleccionÃ³ el viaje");
            sw2=true;
        }

        else if(sw2==false){
            
            int resp = JOptionPane.showConfirmDialog(null, "Â¿EstÃ¡ seguro que desea eliminar el viaje pendiente?", "Alerta!", JOptionPane.YES_NO_OPTION);
            if(resp==0){
                try {
                con = DriverManager.getConnection(url, user, clave);
                stmt = con.createStatement();
                stmt.executeUpdate("DELETE FROM viajes_pendientes WHERE id = '"+jLabelId.getText()+"'");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "error");
                Logger.getLogger(RegistrarTrabajador.class.getName()).log(Level.SEVERE, null, ex);
            }

            JOptionPane.showMessageDialog(null, "Se ha eliminado el gasto exitosamente");
            modelo.setRowCount(0);
            jLabelId.setText("");
            jLabelSumas.setText("");

            refrescarPagina();
            }
            
            
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

