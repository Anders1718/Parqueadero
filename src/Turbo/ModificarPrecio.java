    void cargarDatos(){
        try {
            con = DriverManager.getConnection(url, user, clave);
            stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM precio");
            while(rs.next()){
                datos[0]= rs.getString(1);
                datos[1]= rs.getString(2);
                datos[2]= rs.getString(3);
                datos[3]= rs.getString(4);
                datos[4]= rs.getString(5);



                modelo.addRow(datos);
            }
            TablaDatos.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(Conexion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
    private void jButtonListoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListoActionPerformed
        
            
        if(jTextFieldValor.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No ingres√≥ el valor a modificar");
            
        }else{
                try {
                con = DriverManager.getConnection(url, user, clave);
                stmt = con.createStatement();
                stmt.executeUpdate("UPDATE precio SET "+jComboBoxUnidad.getSelectedItem().toString()+" = '"+jTextFieldValor.getText()+"'"
                        + " WHERE id = '1'");
                } catch (SQLException ex) {
                    Logger.getLogger(ModificarPrecio.class.getName()).log(Level.SEVERE, null, ex);
                }
            
            modelo.setRowCount(0);
            cargarDatos();
            
            JOptionPane.showMessageDialog(null, "Registro exitoso");
            

            jTextFieldValor.setText("");

        }
    }//GEN-LAST:event_jButtonListoActionPerformed

